// Mapeamento name → kebab-case id
function toId(name) {
  return name
    .toLowerCase()
    .replace(/'/g, "")
    .replace(/[\(\)]/g, "")
    .replace(/\./g, "")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "");
}

// Carrega roster atual e pokemon.json pra reusar elements e ids existentes
const existing = JSON.parse(fs.readFileSync(ROSTER_PATH, "utf8"));
const existingByName = new Map(existing.map((p) => [p.name, p]));

// Merge wikiData: name → {tiers per clan, pveRoles, pvpRoles, clans}
const merged = new Map();
for (const [clanName, entries] of Object.entries(wikiData)) {
  for (const entry of entries) {
    let m = merged.get(entry.name);
    if (!m) {
      m = {
        name: entry.name,
        clans: new Set(),
        tiers: [], // {clan, tier}
        pveRoles: new Set(),
        pvpRoles: new Set(),
      };
      merged.set(entry.name, m);
    }
    m.clans.add(clanName);
    m.tiers.push({ clan: clanName, tier: entry.tier });
    const pveR = mapRole(entry.pve);
    const pvpR = mapRole(entry.pvp);
    if (pveR) m.pveRoles.add(pveR);
    if (pvpR) m.pvpRoles.add(pvpR);
  }
}

// Decisão de tier: pega menor rank (T1A é o menor/mais forte)
function pickTier(tiers) {
  return tiers
    .map((t) => t.tier)
    .sort((a, b) => (TIER_RANK[a] ?? 99) - (TIER_RANK[b] ?? 99))[0];
}

// Decisão de role primário (PvE): otdd > burst_dd > offensive_tank > outras
const PVE_ROLE_PRIORITY = ["otdd", "burst_dd", "offensive_tank", "tank", "speedster", "support", "disrupter"];
const PVP_ROLE_PRIORITY = ["otdd", "burst_dd", "offensive_tank", "tank", "speedster", "disrupter", "support"];

function pickRole(roles, priority) {
  if (roles.size === 0) return null;
  for (const r of priority) {
    if (roles.has(r)) return r;
  }
  return null;
}

const newRoster = [];
const seenIds = new Set();
for (const m of merged.values()) {
  const existingPoke = existingByName.get(m.name);
  const id = existingPoke?.id ?? toId(m.name);
  if (seenIds.has(id)) {
    console.warn(`Duplicate id detected: ${id} (${m.name})`);
    continue;
  }
  seenIds.add(id);

  const tier = pickTier(m.tiers);
  const role = pickRole(m.pveRoles, PVE_ROLE_PRIORITY) ?? "burst_dd"; // fallback
  const pvpRole = pickRole(m.pvpRoles, PVP_ROLE_PRIORITY);
  const elements = existingPoke?.elements ?? [];

  newRoster.push({
    id,
    name: m.name,
    tier,
    clans: [...m.clans],
    role,
    pvpRole: pvpRole ?? null,
    elements,
  });
}

// Adiciona pokes existentes do roster que NÃO estão no wiki (edge cases tipo "Crystal Onix")
for (const p of existing) {
  if (!seenIds.has(p.id)) {
    newRoster.push(p);
    seenIds.add(p.id);
  }
}

// Sort por nome
newRoster.sort((a, b) => a.name.localeCompare(b.name));

