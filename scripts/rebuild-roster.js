// Mapeamento role wiki → role tipado
function mapRole(wikiRole) {
  if (!wikiRole || wikiRole === "none" || wikiRole.toLowerCase() === "none") return null;
  const r = wikiRole.toLowerCase().trim();
  // Combos (ex: "BDD/OTDD"): pega o primeiro
  const first = r.split("/")[0].trim();
  if (first === "bdd") return "burst_dd";
  if (first === "otdd") return "otdd";
  if (first === "offensive tank" || first === "offtank") return "offensive_tank";
  if (first === "tank") return "tank";
  if (first === "speedster") return "speedster";
  if (first === "support" || first === "support overtime" || first === "supportot" || first === "support ot") return "support";
  if (first === "disrupter") return "disrupter";
  return null;
}

